package org.cliveford.plateau;

public class Plateau {

    int xAxis;
    int yAxis;


    public Plateau(int xAxisMax, int yAxisMax) {

        xAxis = xAxisMax;
        yAxis = yAxisMax;

    }

    public int getXAxis() {
        return xAxis;
    }

    public int getYAxis() {
        return yAxis;
    }
}
